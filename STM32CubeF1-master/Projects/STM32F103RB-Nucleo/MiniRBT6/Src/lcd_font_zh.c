const uint8_t *LCD_FontZH_Get16(uint16_t unicode)
{
  uint32_t i;

  for (i = 0U; i < (sizeof(g_zh16_table) / sizeof(g_zh16_table[0])); i++)
  {
    if (g_zh16_table[i].unicode == unicode)
    {
      return g_zh16_table[i].bitmap;
    }
  }

  return 0;
}
